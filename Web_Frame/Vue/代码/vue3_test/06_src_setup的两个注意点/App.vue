<template>
  <!-- 自定义事件 -->
  <Demo @hello="showHelloMsg" msg="你好啊" school="尚硅谷">
    <!-- 插槽：在子组件中使用context.slots查看 -->
    <template v-slot:qwe>
      <span>尚硅谷</span>
    </template>
    <template v-slot:asd>
      <span>尚硅谷</span>
    </template>
  </Demo>
</template>

<script>
import Demo from './components/Demo'
export default {
  name: 'App',
  components: { Demo },
  setup() {
    function showHelloMsg(value) {
      alert(`你好啊，你触发了hello事件，我收到的参数是:${value}！`)
    }
    return {
      showHelloMsg,
    }
  },
}
</script>

